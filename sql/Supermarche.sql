CREATE TABLE Pays
(
  id        INT NOT NULL AUTO_INCREMENT,
  nom       VARCHAR(45) NOT NULL,
  codeIso   VARCHAR(45) NOT NULL,
  CONSTRAINT idPays_pk PRIMARY KEY (id),
  CONSTRAINT nom_uk UNIQUE (nom)
