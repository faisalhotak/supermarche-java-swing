DROP DATABASE IF EXISTS Supermarche;
CREATE DATABASE Supermarche CHARACTER SET 'utf8';
USE Supermarche;
SET NAMES utf8;

/* Structure de la table Utilisateurs */
CREATE TABLE Produits
(
    id                 INT NOT NULL AUTO_INCREMENT,
    nom                VARCHAR(45) NOT NULL,
    desc_produit       VARCHAR(70) NOT NULL,
    code_barres        VARCHAR(45) NOT NULL,
    prix               DECIMAL(7,2) NOT NULL,
    taux_tva           DECIMAL(7,2) NOT NULL,
    quantite_produit   INT NOT NULL,
    est_supprime       TINYINT(1) NOT NULL DEFAULT FALSE,
    PRIMARY KEY (id),
    CONSTRAINT unique_nom UNIQUE KEY(nom)
) ENGINE = InnoDB DEFAULT CHARACTER SET = latin1;

CREATE TABLE Postes
(
      id                 INT NOT NULL AUTO_INCREMENT,
      designation        VARCHAR(45) NOT NULL,
      PRIMARY KEY (id)
) ENGINE = InnoDB DEFAULT CHARACTER SET = latin1;

CREATE TABLE Employes
(
      id                 INT NOT NULL AUTO_INCREMENT,
      nom                VARCHAR(45) NOT NULL,
      prenom             VARCHAR(45) NOT NULL,
      id_poste           INT NOT NULL,
      rue                VARCHAR(45) NOT NULL,
      code_postal        VARCHAR(45) NOT NULL,
      localite           VARCHAR(45) NOT NULL,
      pays               VARCHAR(45) NOT NULL,
      num_telephone      VARCHAR(20) NOT NULL,
      num_mobile         VARCHAR(20) NOT NULL,
      salaire            DECIMAL(11,2) NOT NULL,
      email              VARCHAR(45) NOT NULL,
      date_embauche      DATE NOT NULL,
      est_supprime       TINYINT(1) NOT NULL DEFAULT FALSE,
      PRIMARY KEY (id),
      CONSTRAINT fk_Employes_id_poste_id_Postes FOREIGN KEY (id_poste) REFERENCES Postes(id)
) ENGINE = InnoDB DEFAULT CHARACTER SET = latin1;

CREATE TABLE Utilisateurs
(
  idUtilisateur          INT NOT NULL AUTO_INCREMENT,
  nomUtilisateur         VARCHAR(20) NOT NULL,
  mdpUtilisateur         VARCHAR(30) NOT NULL,
  droits                 INTEGER NOT NULL,
  id_employe             INT NOT NULL,
  CONSTRAINT idUtilisateur_pk PRIMARY KEY(idUtilisateur),
  CONSTRAINT fk_Utilisateurs_id_employe_id_Employes FOREIGN KEY (id_employe) REFERENCES Employes(id),
  CONSTRAINT un_nomUtilisateur UNIQUE KEY(nomUtilisateur),
  CONSTRAINT un_id_employe UNIQUE KEY(id_employe)
) ENGINE = InnoDB DEFAULT CHARACTER SET = latin1;

CREATE TABLE Clients
(
      id                 INT NOT NULL AUTO_INCREMENT,
      nom                VARCHAR(45) NOT NULL,
      prenom             VARCHAR(45) NOT NULL,
      rue                VARCHAR(45) NOT NULL,
      code_postal        VARCHAR(45) NOT NULL,
      localite           VARCHAR(45) NOT NULL,
      pays               VARCHAR(45) NOT NULL,
      num_telephone      VARCHAR(20) NOT NULL,
      num_mobile         VARCHAR(20) NOT NULL,
      email              VARCHAR(45) NOT NULL,
      date_inscription   DATE NOT NULL,
      client_fidele      TINYINT(1) NOT NULL,
      est_supprime       TINYINT(1) NOT NULL DEFAULT FALSE,
      PRIMARY KEY (id)
) ENGINE = InnoDB DEFAULT CHARACTER SET = latin1;

CREATE TABLE Modes_Paiement
(
    id                 INT NOT NULL,
    mode               VARCHAR(45) NOT NULL,
    PRIMARY KEY (id)
) ENGINE = InnoDB DEFAULT CHARACTER SET = latin1;

CREATE TABLE Tickets_Caisse
(
    id                  INT NOT NULL AUTO_INCREMENT,
    id_client           INT NOT NULL,
    date_ticket         TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    statut              TINYINT(1) NOT NULL DEFAULT FALSE,
    id_employe          INT NOT NULL,
    PRIMARY KEY (id),
    CONSTRAINT fk_Ticket_Caisse_id_client_id_Client FOREIGN KEY (id_client) REFERENCES Clients (id),
    CONSTRAINT fk_Ticket_Caisse_id_caissier_id_Personnel FOREIGN KEY (id_employe) REFERENCES Employes (id)
) ENGINE = InnoDB DEFAULT CHARACTER SET = latin1;

CREATE TABLE Lignes_Ticket_Caisse
(
    id                      INT NOT NULL AUTO_INCREMENT,
    id_ticket_caisse        INT NOT NULL,
    id_produit              INT NOT NULL,
    quantite_achetee        INT NOT NULL,
    prix_unitaire           DECIMAL(7,2) NOT NULL,
    PRIMARY KEY (id),
    CONSTRAINT fk_Ligne_Ticket_Caisse_id_ticket_caisse_id_Ticket_Caisse FOREIGN KEY (id_ticket_caisse) REFERENCES Tickets_Caisse (id),
    CONSTRAINT fk_Ligne_Ticket_Caisse_id_produit_id_Produit FOREIGN KEY (id_produit) REFERENCES Produits (id)
) ENGINE = InnoDB DEFAULT CHARACTER SET = latin1;

CREATE TABLE Paiements
(
    id                          INT NOT NULL AUTO_INCREMENT,
    id_ticket_caisse            INT NOT NULL,
    id_mode_paiement            INT NOT NULL,
    date_paiement               TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    montant                     DECIMAL(7,2) NOT NULL,
    PRIMARY KEY (id),
    CONSTRAINT fk_id_ticket_caisse_id_Ticket_Caisse FOREIGN KEY (id_ticket_caisse) REFERENCES Tickets_Caisse (id),
    CONSTRAINT fk_Paiement_mode_paiement_type_Paiement FOREIGN KEY (id_mode_paiement) REFERENCES Modes_Paiement (id)
