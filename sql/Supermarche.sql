/* TRIGGERS */

/* Après avoir encode des lignes de ticket, on les retire du stock */
DELIMITER |
DROP TRIGGER IF EXISTS after_create_ligne_ticket_caisse |
CREATE TRIGGER after_create_ligne_ticket_caisse AFTER INSERT ON Lignes_Ticket_Caisse FOR EACH ROW
BEGIN
  UPDATE Produits
  SET quantite_produit = quantite_produit - NEW.quantite_achetee
  WHERE id = NEW.id_produit;
END |
DELIMITER ; 

/* Si l'on décide de supprimer un ticket de caisse, on remet les produits en stock */
DELIMITER |
DROP TRIGGER IF EXISTS after_delete_ligne_ticket_caisse |
CREATE TRIGGER after_delete_ligne_ticket_caisse AFTER DELETE ON Lignes_Ticket_Caisse FOR EACH ROW
BEGIN
  UPDATE Produits
  SET quantite_produit = quantite_produit + OLD.quantite_achetee
  WHERE id = OLD.id_produit;
END |
DELIMITER ; 

/* En supprimant un ticket de caisse, cela enclenchera la suppression des lignes et des paiements associés */
DELIMITER |
DROP TRIGGER IF EXISTS before_delete_tickets_caisse |
CREATE TRIGGER before_delete_tickets_caisse BEFORE DELETE ON Tickets_Caisse FOR EACH ROW
BEGIN
    DELETE FROM Lignes_Ticket_Caisse WHERE id_ticket_caisse = OLD.id;
    DELETE FROM Paiements WHERE id_ticket_caisse = OLD.id;
END|
DELIMITER ;

